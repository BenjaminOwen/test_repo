using System;

namespace my_first_repo
{
	public class Player
	{
						
			public int health;
			public int power;
			public int armor;
			public int speed;
			public int xPos;
			public int yPos;	
			
		public Player ()
		{
			
		}
		
		
		public void GetHealth ()
		
		{
			return health;
		}
		
		public void GetPower ()
		
		{
			return power;
		}
		
		public void GetArmor ()
		
		{
			return armor;
		}
		
		public void GetSpeed ()
		
		{
			return speed;
		}
		
		public void GetXPos ()
		
		{
			return xPos;
		}
		
		public void GetYPos ()
		
		{
			return yPos;
		}
		
	}
}

